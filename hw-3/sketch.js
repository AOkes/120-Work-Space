// Declaring the setup function
function setup() {

    // used to create a canvas that is 600 by 400 pixels
    createCanvas( 600, 400 );

    // used to color the background green
    background( 'green' );
}

// Declaring the draw function
function draw() {

  // telling p5 to print Hello World! on the screen
  // Position of the text will be at (x: 40, y: 40).
  text( "Hello World!", 40, 40);
}
